public class Switch {
    public static void main(String[] args) {
        int option = 7;

        switch(option){
            case 1:
                System.out.println("Pink");
                break;
            case 2:
                System.out.println("Orange");
                break;
            case 3:
                System.out.println("Yellow");
                break;
            default:
                System.out.println("Brown");
        }
    }
}