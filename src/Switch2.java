public class Switch2 {
    public static void main(String[] args) {
        int option = 1;

        switch(option){
            case 1:
                System.out.println("Apple");
                break;
            case 2:
                System.out.println("Orange");
                break;
            case 3:
                System.out.println("Blackberry");
                break;
            default:
                System.out.println("Cherry");
        }
    }
}