public class Characters {
    public static void main(String[] args) {
        char char1 = 'A';
        char char2 = 'B';
        String concatenatedString = Character.toString(char1) + Character.toString(char2);
        System.out.println("Concatenation: " + concatenatedString);
        System.out.println("Character 1: " + char1);
        System.out.println("Character 2: " + char2);
    }
}

